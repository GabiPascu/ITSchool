package session3.challenges;

public class BinaryArithmeticOperators {

    public static void main(String[] args) {

        int number1 = 5;
        int number2 = 10;
        System.out.println("Challenge no. 1:");
        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number1 * number2);
        System.out.println(number2 / number1);
        System.out.println(number1 % number2);

        System.out.println("Are of a rectangle:");

        int length = 2;
        int breadth = 5;

        System.out.println(length * breadth);

        System.out.println("Basic operations between an integer and a double:");
        int number3 = 3;
        double number4 = 9;

        System.out.println(number3 * number4);
        System.out.println(number4 / number3);

        byte x = 12;
        byte y = 24;

        byte sum = (byte) (x+y);
        System.out.println("Storing a result in a byte variable: ");
        System.out.println(sum);
    }

}




